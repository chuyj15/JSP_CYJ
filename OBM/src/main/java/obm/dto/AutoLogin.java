package obm.dto;

public class AutoLogin {

	private int alId;
	private int alSession;
	private String userId;
	private String userPw;
	
	
	public int getAlId() {
		return alId;
	}
	public void setAlId(int alId) {
		this.alId = alId;
	}
	public int getAlSession() {
		return alSession;
	}
	public void setAlSession(int alSession) {
		this.alSession = alSession;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserPw() {
		return userPw;
	}
	public void setUserPw(String userPw) {
		this.userPw = userPw;
	}
	
	
	
	
	
}
